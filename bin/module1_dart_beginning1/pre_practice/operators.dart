void main (){
  //Arithmetic Operator
  var a = 10;
  var b = 6;
  print(a+b);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);

  /*
  Unary Operator:
  1. Postfix (x++, x--)
  2. Prefix (++x, --x)
   */
  var x = 10;
  print(x++); //initially print 10
  print(x++);
  print(x++);
  print(x++);

  var y = 10;
  print(++y); //initially print 11
  print(++y);
  print(++y);
  print(++y);

  var z = 10;
  print(z--); //initially print 10
  print(z--);
  print(z--);
  print(z--);

  var zz = 10;
  print(--zz); //initially print 9
  print(--zz);
  print(--zz);
  print(--zz);

}