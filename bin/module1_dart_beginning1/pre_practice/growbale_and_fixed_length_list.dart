void main (){
  /// Growable List
  var programmingList = ["C", "C++", "Java"];
  print(programmingList);
  programmingList.add("Dart");
  programmingList.add("Python");
  print(programmingList);

  /// Fixed Length List
  const programs = ["C", "C++", "Java", "Dart", "Python"];
  print(programs);
}