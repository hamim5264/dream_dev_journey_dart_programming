void main() {
  int marks = 60;

  switch (marks) {
    case 80:
      print("A+");
      break;

    case 70:
      print("A-");
      break;

    case 60:
      print("B");
      break;

    case 50:
      print("C");
      break;

    case 40:
      print("D");
      break;

    default:
      print("Result Not Found!");
      break;
  }
}
